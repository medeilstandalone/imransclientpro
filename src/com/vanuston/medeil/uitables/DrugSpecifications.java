    public void updateDetails() {
         try {
            if(txtGenericName.getText().equals("")) {
                Alert.inform("Drug Specifications","The details cannot be updated since generic name is empty");
            }
            else {
                ConfirmDialogBox cd = new ConfirmDialogBox(0, "Drug Specifications");
                if(cd.staus==true) {
                    commonController = (CommonImplements) RegistryFactory.getClientStub(RegistryConstants.CommonImplements);
                    String genericName=commonController.getQueryValue("select generic_name from drug_specifications where generic_name='"+jc.getSelectedItem().toString()+"'");
                    Prescription prescriptionController = (Prescription) RegistryFactory.getClientStub(RegistryConstants.PrescriptionFormat);
                    DrugSpecificationModel drugSpecificationModel = new DrugSpecificationModel();
                    if(genericName.equals("")) {
                        drugSpecificationModel.setSaveType("save");
                    }
                    else {
                        drugSpecificationModel.setSaveType("update");
                    }
                    drugSpecificationModel.setGenericName(jc.getSelectedItem().toString());
                    drugSpecificationModel.setDescription(txtDescription.getText().trim());
                    drugSpecificationModel.setCategories(txtCategories.getText().trim());
                    drugSpecificationModel.setChemicalFormula(txtChemicalFormula.getText().trim());
                    drugSpecificationModel.setIndication(txtIndication.getText().trim());
                    drugSpecificationModel.setPharmaCodynamics(txtPharmacoDynamics.getText().trim());
                    drugSpecificationModel.setMechanism(txtMechanism.getText().trim());
                    drugSpecificationModel.setVolumeDistribution(txtVolumeDistribution.getText().trim());
                    drugSpecificationModel.setMetabolism(txtMetabolism.getText().trim());
                    drugSpecificationModel.setRouteElimination(txtRouteElimination.getText().trim());
                    drugSpecificationModel.setAbsorption(txtAbsorption.getText().trim());
                    drugSpecificationModel.setHalfLife(txtHalfLife.getText().trim());
                    drugSpecificationModel.setToxicity(txtToxicity.getText().trim());
                    drugSpecificationModel.setFoodInteractions(txtFoodInteraction.getText().trim());
                    int i=prescriptionController.insertDrugSpecification(drugSpecificationModel);
                    if(i > 0)
                    JOptionPane.showMessageDialog(rootPane, "Data saved successfully");
                }
            }
        } catch (Exception e) {
            String msg = "Class : DrugSpecifications  Method : displayItemDetails  = " + e.getMessage();
            log.debug(msg);
        }
    }

