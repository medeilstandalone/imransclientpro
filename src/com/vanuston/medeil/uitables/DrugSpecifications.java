    public void loadSpecifications(String itemName,String foodInteraction) {
        try {
            if(foodInteraction.equals("yes")) {
                removeComponents();
            }
            commonController = (CommonImplements) RegistryFactory.getClientStub(RegistryConstants.CommonImplements);
            String genericNames = commonController.getQueryValue("SELECT  genericname from drugtable where concat(itemname,'_',dosage,' |',formulation)='"+itemName+"' limit 1");
            String[] genericName= genericNames.split(",");            
            for(int i=0; i <genericName.length; i++) {
                jc.addItem(genericName[i]);
                jc.setPreferredSize(new Dimension(350, jc.getPreferredSize().height));
                jPanel1.add(jc,new org.netbeans.lib.awtextra.AbsoluteConstraints(600, 50, 118, -1));
            }            
            txtGenericName.setText(genericNames);
            displayItemDetails();
        }
        catch(Exception e) {
            String msg = "Class : DrugSpecifications  Method : loadSpecifications  = " + e.getMessage();
            log.debug(msg);
        }
    }

